// Create the theme store
const useThemeStore = create((set) => {
  // Get the user's preferred theme from local storage or default to system preference
  const savedThemeMode = localStorage.getItem('themeMode') || 
    (window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light');
  
  return {
    // State
    themeMode: savedThemeMode,
    theme: savedThemeMode === 'dark' ? darkTheme : lightTheme,
    
    // Actions
    toggleTheme: () => {
      set((state) => {
        const newMode = state.themeMode === 'light' ? 'dark' : 'light';
        localStorage.setItem('themeMode', newMode);
        return {
          themeMode: newMode,
          theme: newMode === 'dark' ? darkTheme : lightTheme
        };
      });
    },
    
    // Set theme mode explicitly
    setThemeMode: (mode) => {
      const validMode = mode === 'dark' ? 'dark' : 'light';
      localStorage.setItem('themeMode', validMode);
      set({
        themeMode: validMode,
        theme: validMode === 'dark' ? darkTheme : lightTheme
      });
    }
  };
});

export default useThemeStore; 